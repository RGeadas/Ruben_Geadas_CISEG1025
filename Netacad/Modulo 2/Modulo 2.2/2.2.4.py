print("I like \"Monty Python\"") #aqui usamos os backslash (escape character) para representar as quotes dentro da string
print('I like "Monty Python"') #uma maneira mais facil para representar uma quote dentro duma string

print('I\'m Monty Python.') #usamos o (\) para dar "escape" no ' do I'm (não gera erro) quando temos a string entre ''
print("I'm Monty Python.") #outra maneira para que esta string não dê erro, usando desta vez as aspas "" e o ' apenas no I'm
print ("") #isto continua a ter um valor de uma string (vazia) mas usando as ""
print ('') #isto continua a ter um valor de uma string (vazia) mas usando os ''